#we can use a type of string called f-string that helps us format our strings much more easily.#

first_name = "Bhavesh"
favourite_language = "python"

#To define an f-string, we just add an f before the single or double quotes. Then, within the string, we surround the variables or expressions with curly braces {}.#

print(f"Hi I am {first_name}.I am learing {favourite_language}")

#output = Hi I am Bhavesh. I am learing python #